							<h1>World unites in 'Prayer for Humanity'</h1>
							<p>May 14, 2020</p>
							<img src="img/home-news/world-unite.jpg" width="100%">
							<br/>
							<br/>
							<p style="text-align: justify; background: white; vertical-align: baseline; margin: 0in 0in 11.25pt 0in;"><span class="ms-rtefontsize-2"><span lang="DE" style="font-size: 10.0pt; font-family: 'Arial',sans-serif; color: #666666;">Tomorrow, 14th May 2020, will witness a global united effort where individuals from all walks of life and belief systems will come together in reflection and prayer.</span></span></p>
							<p style="text-align: justify; background: white; vertical-align: baseline; margin: 0in 0in 11.25pt 0in;"><span class="ms-rtefontsize-2"><span lang="DE" style="font-size: 10.0pt; font-family: 'Arial',sans-serif; color: #666666;">The Higher Committee for Human Fraternity, HCHF, initiated the 'Prayer for Humanity' call, inviting religious leaders and faithful around the world to a day of fasting, prayers and supplications for "the good of all humanity" and "an end to the COVID-19 pandemic".</span></span></p>
							<p style="text-align: justify; background: white; vertical-align: baseline; margin: 0in 0in 11.25pt 0in;"><span class="ms-rtefontsize-2"><span lang="DE" style="font-size: 10.0pt; font-family: 'Arial',sans-serif; color: #666666;">Commenting on the initiative, the HCHF Secretary-General Judge Mohamed Abdelsalam said that following the launch of the action on 2nd May 2020, the Committee had received global support from a large number of religious, political leaders and institutions from all over the world. Led by Sheikh Dr. Ahmed el-Tayyeb, Grand Imam of Al Azhar, and His Holiness Pope Francis, Head of the Catholic Church, many state presidents, prime ministers, along with religious leaders, political figures, media personalities, social influencers and prestigious educational institutions have also supported the call to pray for humanity, Abdelsalam told the Emirates News Agency, WAM.</span></span></p>
							<p style="text-align: justify; background: white; vertical-align: baseline; margin: 0in 0in 11.25pt 0in;"><span class="ms-rtefontsize-2"><span lang="DE" style="font-size: 10.0pt; font-family: 'Arial',sans-serif; color: #666666;">He extended the Higher Committee's appreciation and gratitude to His Highness Sheikh Mohamed bin Zayed Al Nahyan, Crown Prince of Abu Dhabi and Deputy Supreme Commander of the UAE Armed Forces, for his continued efforts to bring the global community together, and for his support of the 'Prayer for Humanity' initiative.</span></span></p>
							<p style="text-align: justify; background: white; vertical-align: baseline; margin: 0in 0in 11.25pt 0in;"><span class="ms-rtefontsize-2"><span lang="DE" style="font-size: 10.0pt; font-family: 'Arial',sans-serif; color: #666666;">Abdelsalam explained that world peace and human fraternity form core pillars of UAE society, that has carried forward the legacy of the late Sheikh Zayed bin Sultan Al Nahyan, who created a nation that has become host to individuals from all walks of life, coexisting peacefully. He went on to note that the UAE helped oversee the creation of the Human Fraternity Document, adding that it is a true testament of the UAE leadership and its people's belief in unity, fraternity, and peace for all.</span></span></p>
							<p style="text-align: justify; background: white; vertical-align: baseline; margin: 0in 0in 11.25pt 0in;"><span class="ms-rtefontsize-2"><span lang="DE" style="font-size: 10.0pt; font-family: 'Arial',sans-serif; color: #666666;">For his part, Dr. Sultan Al Remeithi, Secretary-General of the Muslim Council of Elders and member of the HCHF, noted the need for a silver lining, a positive recognition during the coronavirus pandemic. The Prayer for Humanity has sparked hope, he said, adding that the call has united prayers and hearts around the world for the common good.</span></span></p>
							<p style="text-align: justify; background: white; vertical-align: baseline; margin: 0in 0in 11.25pt 0in;"><span class="ms-rtefontsize-2"><span lang="DE" style="font-size: 10.0pt; font-family: 'Arial',sans-serif; color: #666666;">Dr. Al Remeithi noted that the global call for prayer seeks God the Almighty's salvation from the COVID-19 pandemic, as well as ridding global communities from the scourge of hate, discrimination and antipathy.</span></span></p>
							<p style="text-align: justify; background: white; vertical-align: baseline; margin: 0in 0in 11.25pt 0in;"><span class="ms-rtefontsize-2"><span lang="DE" style="font-size: 10.0pt; font-family: 'Arial',sans-serif; color: #666666;">Yasser Hareb, Emirati Writer and TV Presenter, and HCHF member, stressed that human solidarity is a value that transcends all limits. "On Thursday we will raise our voices to God Almighty in prayer and supplication to protect humanity from this pandemic as we have witnessed how harm afflicting one person, can in turn harm all of humanity."</span></span></p>
							<p style="text-align: justify; background: white; vertical-align: baseline; margin: 0in 0in 11.25pt 0in;"><span class="ms-rtefontsize-2"><span lang="DE" style="font-size: 10.0pt; font-family: 'Arial',sans-serif; color: #666666;">Father Professor Dr. Ioan Sauca, Secretary-General of the World Council of Churches, WCC, and HCHF member, had called on members of the WCC to join in the global day of prayer. He noted, "Due to this pandemic, many of our people suffer from a state of fear, mistrust, shock, isolation, social distancing and have even experienced deaths within their families or their ecclesiastical communities. While we continue to suffer from this global health crisis, our global solidarity through prayer will reflect our innate feeling of responsibility and care towards one another."</span></span></p>
							<p style="text-align: justify; background: white; vertical-align: baseline; margin: 0in 0in 11.25pt 0in;"><span class="ms-rtefontsize-2"><span lang="DE" style="font-size: 10.0pt; font-family: 'Arial',sans-serif; color: #666666;">The Higher Committee of Human Fraternity seeks to bring humanity together in prayer on Thursday, 14th May, in what will be the largest gathering of humanity for one goal. The event will be accompanied by unprecedented media coverage, through the Committee&rsquo;s social media accounts with two hashtags in Arabic and English, #ØµÙ„Ø§Ø©_Ù…Ù†_Ø£Ø¬Ù„_Ø§Ù„Ø¥Ù†Ø³Ø§Ù†ÙŠØ© and #PrayForHumanity to allow people to interact and share their videos, photos and posts.</span></span></p>
