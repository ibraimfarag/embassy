							<h1>His Highness Sheikh Mohamed bin Zayed and Merkel review bilateral ties, regional development.</h1>
							<p>January 19, 2020</p>
							<img src="img/home-news/bilateral.jpg" width="100%">
							<br/>
							<br/>
							<p style="background: white; margin: 0in 0in 7.5pt 0in;"><span class="ms-rtefontface-1"><span lang="DE" style="font-size: 10.0pt; font-family: 'Arial',sans-serif; color: #666666;">His Highness Sheikh Mohamed bin Zayed Al Nahyan, Crown Prince of Abu Dhabi and Deputy Supreme Commander of the UAE Armed Forces, held a meeting with the German Chancellor, Angela Merkel, during which they discussed bilateral ties and ways of developing them at all levels. The leaders also spoke about the latest developments in the Gulf and the Middle East, especially Libya, besides issues of mutual interest.</span></span></p>
							<p style="background: white; margin: 0in 0in 7.5pt 0in;"><span class="ms-rtefontface-1"><span lang="DE" style="font-size: 10.0pt; font-family: 'Arial',sans-serif; color: #666666;">The meeting was attended by H.H. Sheikh Abdullah bin Zayed Al Nahyan, Minister of Foreign Affairs and International Cooperation, who will be leading the UAE delegation to the Berlin Conference to be held tomorrow. Dr Sultan bin Ahmad Al Jaber, Minister of State and Special Envoy of the UAE to the Federal Republic of Germany, was also present.</span></span></p>
							<p style="background: white; margin: 0in 0in 7.5pt 0in;"><span class="ms-rtefontface-1"><span lang="DE" style="font-size: 10.0pt; font-family: 'Arial',sans-serif; color: #666666;">At the start of the meeting, Sheikh Mohamed bin Zayed expressed his thanks to the German Chancellor for the warm reception and the efforts being made to strengthen bilateral ties. He wished the friendly German people more progress and development.</span></span></p>
							<p style="background: white; margin: 0in 0in 7.5pt 0in;"><span class="ms-rtefontface-1"><span lang="DE" style="font-size: 10.0pt; font-family: 'Arial',sans-serif; color: #666666;">His Highness Sheikh Mohamed said the UAE and Germany were major strategic partners, and their bilateral relations were growing thanks to the desire of the leadership of both countries to push forward ties in political, economic, cultural and other fields.</span></span></p>
							<p style="background: white; margin: 0in 0in 7.5pt 0in;"><span class="ms-rtefontface-1"><span lang="DE" style="font-size: 10.0pt; font-family: 'Arial',sans-serif; color: #666666;">Sheikh Mohamed bin Zayed said the security and stability of the Middle East is in the region and world's interest, given its global strategic importance. He added that the UAE felt great wisdom needs to be exercised in dealing with developments in this region.</span></span></p>
							<p style="background: white; margin: 0in 0in 7.5pt 0in;"><span class="ms-rtefontface-1"><span lang="DE" style="font-size: 10.0pt; font-family: 'Arial',sans-serif; color: #666666;">His Highness said Germany, which enjoyed considerable regional and global influence as well as good relations with most countries, had an important role to play in working for peace and stability in the region. He said both the UAE and Germany played a vital role in trying to achieve regional and international stability.</span></span></p>
							<p style="background: white; margin: 0in 0in 7.5pt 0in;"><span class="ms-rtefontface-1"><span lang="DE" style="font-size: 10.0pt; font-family: 'Arial',sans-serif; color: #666666;">Sheikh Mohamed bin Zayed expressed the UAE&rsquo;s appreciation for the positive and constructive role played by Germany in seeking to find a political settlement to the crisis in Libya through the Berlin Conference which will be held tomorrow. He affirmed the UAE&rsquo;s full support for the efforts by Germany and the United Nations envoy to Libya to achieve a ceasefire and reach a comprehensive political settlement which would strengthen Arab security and maintain stability in the Mediterranean.</span></span></p>
							<p style="background: white; margin: 0in 0in 7.5pt 0in;"><span class="ms-rtefontface-1"><span lang="DE" style="font-size: 10.0pt; font-family: 'Arial',sans-serif; color: #666666;">"A political and peaceful solution is the best approach for achieving security and stability in the region and fulfilling the aspirations of the Libyan people", His Highness said.</span></span></p>
							<p style="background: white; margin: 0in 0in 7.5pt 0in;"><span class="ms-rtefontface-1"><span lang="DE" style="font-size: 10.0pt; font-family: 'Arial',sans-serif; color: #666666;">His Highness said the UAE supported any initiative that would resolve the crisis and help Libya to overcome its suffering by putting an end to interference in its internal affairs, strengthen the pillars of the state and stop the inflow of terrorists into Libya. He added that the UAE has always been on the side of the Libyan people and fully supported their legitimate aspirations for peace, reconciliation, unity and development.</span></span></p>
							<p style="background: white; margin: 0in 0in 7.5pt 0in;"><span class="ms-rtefontface-1"><span lang="DE" style="font-size: 10.0pt; font-family: 'Arial',sans-serif; color: #666666;">Sheikh Mohamed bin Zayed said: "The UAE looks forward to Chancellor Angela Merkel leading the Berlin Conference on Libya, with such a large and influential international and regional presence, to provide a strong impetus for achieving its goals of returning to constructive dialogue and achieve peace and security in Libya."</span></span></p>
							<p style="background: white; margin: 0in 0in 7.5pt 0in;"><span class="ms-rtefontface-1"><span lang="DE" style="font-size: 10.0pt; font-family: 'Arial',sans-serif; color: #666666;">The two sides agreed on the importance of building Libyan national institutions and restoring law and order to fight the forces of extremism and terrorism and to counter foreign interference in Libya.</span></span></p>
							<p style="background: white; margin: 0in 0in 7.5pt 0in;"><span class="ms-rtefontface-1"><span lang="DE" style="font-size: 10.0pt; font-family: 'Arial',sans-serif; color: #666666;">For her part, Chancellor Merkel thanked Sheikh Mohamed bin Zayed for visiting Germany and for his important role in enhancing the UAE-German friendship. Merkel said she was keen to keep consulting with His Highness on matters relating to the Gulf and Middle East, and added that the UAE and Germany worked together for peace and development, promoting tolerance and coexistence, and opposing extremism and terrorism.</span></span></p>
